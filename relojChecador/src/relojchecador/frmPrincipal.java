    public void cargardatos() {
        try {
            String id_registro, id_trabajador, nombre, fecha, hora, entrada_salida;
            Class.forName("com.mysql.jdbc.Driver");
            Connection conexion = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/bd_rchecador", "root", "diegogarcia");
            Statement st = (Statement) conexion.createStatement();
            ResultSet rs = (ResultSet) st.executeQuery("select*from bd_reloj_checador");
            while (rs.next()) {
//                dato= rs.getString("dato");
                id_registro = rs.getString("id_registro");
                id_trabajador = rs.getString("id_trabajador");
                nombre = rs.getString("nombre");
                fecha = rs.getString("fecha");
                hora = rs.getString("hora");
                entrada_salida = rs.getString("entrada_salida");
                tabladatos.addRow(new Object[]{id_registro, id_trabajador, nombre, fecha, hora, entrada_salida});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "" + e.getMessage());
        }
    }
