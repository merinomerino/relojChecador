    public void filtro() {
        int columnaABuscar = 0;
        if (comboFiltro.getSelectedItem() == "id_registro") {
            columnaABuscar = 0;
        }
        if (comboFiltro.getSelectedItem().toString() == "id_trabajador") {
            columnaABuscar = 1;
        }
        if (comboFiltro.getSelectedItem() == "nombre") {
            columnaABuscar = 2;
        }
        if (comboFiltro.getSelectedItem() == "fecha") {
            columnaABuscar = 2;
        }
        if (comboFiltro.getSelectedItem() == "hora") {
            columnaABuscar = 2;
        }
        trsFiltro.setRowFilter(RowFilter.regexFilter(txtFiltro.getText(), columnaABuscar));
    }

